{
    let n = 5
    for (var i = 1; i <= n; i++) 
    {
      var p = '';

        for (var j = 1; j <= (n - i); j++) 
            {
            p += ' '; 
            }

        for (var k = 1; k <= i; k++) {
            p += '#';
            }

        p += " "

        for (var m = 1; m <= i; m++) {
            p += "#"    
            }
        
      console.log(p);
    }
  }
